package org.kcs.chatdisplay.model;

public class CustomCodeWindow {
	private boolean visible;
	private int width;
	public boolean isVisible() {
		return visible;
	}
	public void setVisible(boolean visible) {
		this.visible = visible;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
}
